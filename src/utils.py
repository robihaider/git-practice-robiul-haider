def add(a, b):
    return a + b
# Created subtraction functions in utils.py

def subtract(a, b):
    return a - b

# Created Multiply functions in utils.py

def multiply(a, b):
    return a * b

# Created Divide functions in utils.py

def divide(a, b):
    if b == 0:
        return "Error: Division by zero"
    return a / b

